#Questão: Escreva um programa que leia dois números e informe se eles são iguais.

número1 = int(input("Digite um número: "))
número2 = int(input("Digite outro número: "))
print()

if número1 == número2:
    print("Os números digitados são iguais!")
else:
    print("Os números digitados não são iguais!")